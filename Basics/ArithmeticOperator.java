package Basics;

public class ArithmeticOperator {
    public static void main(String[] args) {
        int a=20;
        int b=10;
        int ad=a+b;
        int s=a-b;
        int d=a/b;
        int m=a%b;
        System.out.println(ad);
        System.out.println(s);
        System.out.println(d);
        System.out.println(m);
    }
    
}
